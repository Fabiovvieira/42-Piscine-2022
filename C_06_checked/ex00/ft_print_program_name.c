#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int c, char **v)
{
	int	i;

	i = 0;
	while (v[0][i] != '\0' && c)
	{
		ft_putchar((v[0][i]));
		i++;
	}
	ft_putchar('\n');
	return (0);
}
