#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int c, char **v)
{
	int	i;

	i = 0;
	if (c == 2)
	{
		while(v[1][i] == ' ' || v[1][i] == '\t')
			i++;
		while(v[1][i] != ' ' && v[1][i] != '\t' & v[1][i] != '\0')
		{
			ft_putchar(v[1][i]);
			i++;
		}		
	}
	ft_putchar('\n');
	return (0);
}
