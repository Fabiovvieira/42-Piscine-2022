#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	char	c;

	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		ft_putnbr(147483648);
	}
	else if (nb < 0)
	{
		nb = -nb;
		ft_putchar('-');
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		ft_putchar(nb + 48);
	}
}

int main()
{
    // int a = 147483648;
    // int a = 2147483647;
    // int a=-215;
    ft_putnbr(0);
    ft_putnbr(-2147483648);
    ft_putnbr(2147483647);
    ft_putnbr(147483648);
    return (0);
}